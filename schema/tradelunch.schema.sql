-- ===========================
-- FILES TABLE
-- ===========================
-- Maps to MCPFileInfo in schema.py:
--   original_filename: Original filename from local filesystem
--   stored_name: Slug-based filename for storage (e.g., "article-slug.png")
--   s3_key: Full S3 key path (e.g., "4/tech/ai/article-slug/article-slug.png")
--   s3_url: Full CDN URL (stored_uri alias)
--   content_type: MIME type
--   ext: File extension without dot
--   file_size: Size in bytes
--   is_thumbnail: Whether this is the post thumbnail
drop table if exists files;
CREATE TABLE if not exists files (
    seq int8 GENERATED ALWAYS AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 9223372036854775807 START 1 CACHE 1 NO CYCLE) NOT NULL,
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         BIGINT,
    post_id         BIGINT,
    content_type    VARCHAR(100),
    ext             VARCHAR(30),
    original_filename   VARCHAR(255) NOT NULL,
    stored_name         VARCHAR(255) NOT NULL,
    s3_key              TEXT NULL,              -- S3 key path (e.g., "user_id/category/.../slug/filename")
    stored_uri          TEXT NOT NULL,          -- Full CDN URL (s3_url)
    file_size           INTEGER,
    is_thumbnail        BOOLEAN DEFAULT FALSE,
    created_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted_at          TIMESTAMP DEFAULT NULL,
    UNIQUE(user_id, stored_name)
);

